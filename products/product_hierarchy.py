# Helper functions for working with the hierarchy
def get_all_brands():
    """Get all brand names"""
    return list(PRODUCT_HIERARCHY.keys())

def get_brand_categories(brand_name):
    """Get all categories for a specific brand"""
    return list(PRODUCT_HIERARCHY.get(brand_name, {}).keys())

def get_category_collections(brand_name, category_name):
    """Get all collections for a specific brand and category"""
    brand_data = PRODUCT_HIERARCHY.get(brand_name, {})
    category_data = brand_data.get(category_name, {})
    
    if isinstance(category_data, dict):
        return list(category_data.keys())
    else:
        return []

def get_hierarchy_for_brand(brand_name):
    """Get complete hierarchy for a specific brand"""
    return PRODUCT_HIERARCHY.get(brand_name, {})

def get_flat_structure():
    """Get a flattened structure for easier template rendering"""
    flat_structure = []
    
    for brand, categories in PRODUCT_HIERARCHY.items():
        brand_data = {
            'name': brand,
            'categories': []
        }
        
        for category, collections in categories.items():
            category_data = {
                'name': category,
                'collections': []
            }
            
            if isinstance(collections, dict):
                for collection in collections.keys():
                    category_data['collections'].append(collection)
            
            brand_data['categories'].append(category_data)
        
        flat_structure.append(brand_data)
    
    return flat_structure 